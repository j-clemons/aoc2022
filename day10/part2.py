INPUT_TXT = os.path.join(os.path.dirname(__file__), 'input.txt')


def update_crt(cycle: int, sprite_center: int) -> tuple [int, int, str]:
    row = (cycle - 1) // 40
    pos = (cycle - 1) - (row * 40)

    pixel = '.'
    if pos in [sprite_center - 1, sprite_center, sprite_center + 1]:
        pixel = '#'

    return (row, pos, pixel)


def compute(s: str) -> int:
    sp_ctr = 1
    cycle = 0

    crt = [['.' for x in range(0,40)] for y in range(0,6)]

    lines = s.splitlines()
    for line in lines:
        l = line.split(' ')

        cmd = l[0]
        if cmd == 'noop':
            cycle += 1

            r, p, px = update_crt(cycle, sp_ctr)
            crt[r][p] = px

        elif cmd == 'addx':
            cycle += 1
            r, p, px = update_crt(cycle, sp_ctr)
            crt[r][p] = px


            cycle += 1
            r, p, px = update_crt(cycle, sp_ctr)
            crt[r][p] = px

            sp_ctr += int(l[1])


    for i in crt:
        print(''.join(i))

    return 0


